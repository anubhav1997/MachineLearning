def sigmoid(z):
    return 1.0/(1.0+np.exp(-z))

def sigmoid_prime(z):
    return sigmoid(z)*(1-sigmoid(z))


def ReLU(x):
    return x * (x > 0)

def ReLU_prime(x):
    return 1. * (x > 0)



